# PROGRAM TO SHOWCASE THE WORKING OF DIFFERENRT TYPES OF BITWISE OPERATORS
num1 = 10
num2 = 4
# USING AND OPERATOR
print("num1 & num2 =", num1 & num2)
# USING OR OPERATOR
print("num1 | num2 =", num1 | num2)
# USING NOT OPERATOR
print("~num1 =", num1)
# USING XOR OPERATOR
print("num1 ^ num2 =",num1 ^ num2)
num1 = 10
num2 = 4
# USING RIGHT SHIFT OPERATOR ON num1
print("num1 >> 1 =", num1 >> 1)
# USING RIGHT SHIFT OPERATOR ON num2
print("num2 >> 1 =", num2 >> 1)
num1 = 10
num2 = 4
# USING LEFT SHIFT OPERATOR ON num1
print("num1 << 1 =", num1 << 1)
# USING LEFT SHIFT OPERATOR ON num2
print("num2 << 1 =", num2 << 1)